    public function searchFor(string $query)
    {
        return $this->createQueryBuilder('d')
            ->andWhere('LOWER(d.title) LIKE LOWER(:q)')
            ->setParameter('q', '%' . $query . '%')
            ->orderBy('d.id', 'ASC')
            ->getQuery()
            ->getResult();
